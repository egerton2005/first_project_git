def area(a, b): 
    '''
    Get value a, b (int) - 
        lenght side of rectangle
    
    Return area (int) 
        rectangle by formula a*b
    '''
    return a * b 

def perimeter(a, b):
    '''
    Get value a, b (int) - 
        lenght side of rectangle
    
    Return perimeter (int) 
        rectangle by formula (a+b)*2
    ''' 
    return (a + b)*2 
