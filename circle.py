import math


def area(r):
    '''
    Get value r (int) - radius
    
    Return area (int) 
        circle by formula pi*r**2
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Get value r (int) - radius
    
    Return perimeter (int)
        circle by formula pi*r*2
    '''
    return 2 * math.pi * r

