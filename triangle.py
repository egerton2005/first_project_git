def area(a, h): 
    '''
    Get value a, h (int) - 
        lenght side and hight of triangle
    
    Return area (int) 
        rectangle by formula a*h/2
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Get value a, b, c (int) - 
        lenght side of triangle
    
    Return perimeter (int) 
        triangle by formula a+b+c
    ''' 
    return a + b + c 
