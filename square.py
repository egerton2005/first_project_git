
def area(a):
    '''
    Get value a (int) - 
        lenght side of square
    
    Return area (int) 
        square by formula a*a
    '''
    return a * a


def perimeter(a):
    '''
    Get value a (int) - 
        lenght side of square
    
    Return perimeter (int) 
        square by formula 4*a
    ''' 
    return 4 * a
